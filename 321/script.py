class Solution(object):
    def maxNumber(self, nums1, nums2, k):
        """
        :type nums1: List[int]
        :type nums2: List[int]
        :type k: int
        :rtype: List[int]
        """
        N1 = len(nums1)
        N2 = len(nums2)

        def best(nums, k):
            a = []
            for i, x in enumerate(nums):
                while a and a[-1] < x and len(nums) - i + len(a) -1 >= k:
                    a.pop()
                if len(a) < k:
                    a.append(x)
            return a

        ret = None
        for k1 in xrange(max(0, k - N2), min(N1, k) + 1):
            k2 = k - k1
            a1 = best(nums1, k1)
            a2 = best(nums2, k2)
            res = []
            while a1 or a2:
                if a1 > a2:
                    res.append(a1[0])
                    a1 = a1[1:]
                else:
                    res.append(a2[0])
                    a2 = a2[1:]

            if ret is None or ret < res:
                ret = res
        return ret


